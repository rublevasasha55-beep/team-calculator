# Модуль с математическими операциями
# Каждый участник реализует свою функцию

def add(a, b):
    """Функция сложения двух чисел."""
    return a + b

def subtract(a, b):
    """Функция вычитания двух чисел."""
    return a - b

def multiply(a, b):
    """Функция умножения двух чисел."""
    return a * b

